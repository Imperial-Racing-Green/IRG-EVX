function [F_y,F_x,M_z,M_y,M_xF,M_xR,F_z] = Chassis7(g,m_C,h_C,m_CF,m_CR,...
    h_CF,h_CR,a,b,d_F,e_F,d_R,e_R,F_yFR,F_yFL,F_yRR,F_yRL,F_xFR,F_xFL,...
    F_xRR,F_xRL,M_zFR,M_zFL,M_zRR,M_zRL,S_FR,S_FL,S_RR,S_RL,m_WRL,m_WRR,...
    m_WFL,m_WFR)

F_y = F_yRL * cos(S_RL) + F_yRR * cos(S_RR) + F_yFL * cos(S_FL) + ...
    F_yFR * cos(S_FR)...
     - F_xRL * sin(S_RL) - F_xRR * sin(S_RR) + F_xFL * sin(S_FL) + ...
    F_xFR * sin(S_FR);

F_x = F_xRL * cos(S_RL) + F_xRR * cos(S_RR) + F_xFL * cos(S_FL) + ...
    F_xFR * cos(S_FR)...
     + F_yRL * sin(S_RL) + F_yRR * sin(S_RR) - F_yFL * sin(S_FL) - ...
    F_yFR * sin(S_FR);

F_z = -g * m_C;

M_z = F_xRR * (sin(S_RR) * b + cos(S_RR) * e_R) +...
    F_yRR * (sin(S_RR) * e_R - cos(S_RR) * b) +...
    F_xFR * (cos(S_FR) * e_F + sin(S_FR) * a) +...
    F_yFR * (cos(S_FR) * a - sin(S_FR) * e_F) +...
    F_xFL * (-cos(S_FL) * d_F + sin(S_FL) * a) +...
    F_yFL * (cos(S_FL) * a + sin(S_FL) * d_F) +...
    F_xRL * (-cos(S_RL) * d_R + sin(S_RL) * b) +...
    F_yRL * (-sin(S_RL) * d_R - cos(S_RL) * b) +...
    + M_zFL + M_zRL + M_zRR + M_zFR;

m = m_C + m_WRL + m_WRR + m_WFL + m_WFR;
a_x = F_x / m;
M_y = m_C * a_x * h_C;
M_xF = m_CF * a_y * h_CF;
M_xR = m_CR * a_y * h_CR;


